# Store a string inside a variable named 'sentence'
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."
print(sentence)
print("")
# Replace all the instances where an exclamation mark occurs with a space
new_sentence = sentence.replace("!", " ")
print(new_sentence)
print("")
# Convert this sentence with replaced exclamation to all capital letters
new_sentence = new_sentence.upper()
print(new_sentence)
print("")
# Reverse the order of the sentence
print(new_sentence[::-1])

